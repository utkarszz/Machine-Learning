# Lists
nums = [1, 2, 3, 4, 5]
nums.append(6)  # Adding an element
nums.remove(3)  # Removing an element
print(nums)
print(nums[0])  # Accessing an element
print(nums[1:4])  # Slicing
print(len(nums))  # Length of the list

# Tuples
point =(2,3)
print(point)
print(point[0])  # Accessing an element

# SETS
s = {1, 2, 3, 4,4, 5}
